(function ($) {
    $.fn.serializeJson = function () {
        var serializeObj = {};
        var array = this.serializeArray();
        var str = this.serialize();
        $(array).each(function () {
            if (serializeObj[this.name]) {
                if ($.isArray(serializeObj[this.name])) {
                    serializeObj[this.name].push(this.value);
                } else {
                    serializeObj[this.name] = [serializeObj[this.name], this.value];
                }
            } else {
                serializeObj[this.name] = this.value;
            }
        });
        return serializeObj;
    };
})(jQuery);




String.prototype.trim = function () {
    return this.replace(/(^\s*)|(\s*$)/g, "");
}
String.prototype.ltrim = function () {
    return this.replace(/(^\s*)/g, "");
}
String.prototype.rtrim = function () {
    return this.replace(/(\s*$)/g, "");
}


function showmessageWindow(message) {
    var left = ($(window).width() - $('.messageWindow').width()) / 2;
    var top = ($(window).height() - $('.messageWindow').height()) / 3;
    $('.messageWindow span').text(message)
    $('.messageWindow').css('left', left).css('top', top).fadeIn(1000, function () {
        $('.messageWindow').fadeOut(1000);
    })
}